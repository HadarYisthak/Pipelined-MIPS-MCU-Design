.data
Mat1:   .word 1, 100, 3, 4, 90, 6, 7, 8, 34, 23, 11, 12, 13, 1, 2, 16
Mat2:   .word 13, 14, 15, 16, 9, 10, 11, 12, 5, 6, 7, 8, 1, 2, 3, 4
resMat: .space 64       # מקום ל־16 תוצאות (16 * 4 בתים)

.text
.globl main

main:
    lui $1, 0x1000             # טוען את הבסיס של האזור בכתובת גבוהה (אם נדרש)
    ori $2, $1, 0x0000         # $2 מצביע ל־Mat1
    ori $3, $1, 0x0040         # $3 מצביע ל־Mat2
    ori $4, $1, 0x0080         # $4 מצביע ל־resMat
    addi $5, $zero, 0          # i = 0

loop:
    lw $6, 0($2)               # טען ערך מ־Mat1
    lw $7, 0($3)               # טען ערך מ־Mat2
    slt $7, $7, $6             # חיבור שני הערכים
    sw $7, 0($4)               # שמור את הסכום ב־resMat
    addi $2, $2, 4             # קידום מצביע Mat1
    addi $3, $3, 4             # קידום מצביע Mat2
    addi $4, $4, 4             # קידום מצביע resMat
    addi $5, $5, 1             # i++
    slti $6, $5, 16            # אם i < 16
    bne $6, $zero, loop        # חזור ללולאה אם כן

end:
    j end                     # עצירה בלולאה אינסופית
